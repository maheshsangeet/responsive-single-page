function openNav() {
  document.querySelector(".side-bar").style.width = "200px";
  document.querySelector(".side-bar").style.height = "100vh" ;
  document.querySelector(".side-bar").style.padding = "30px 50px" ;

}

function closeNav() {
  document.querySelector(".side-bar").style.width = "0";
  document.querySelector(".side-bar").style.padding= "0";
}